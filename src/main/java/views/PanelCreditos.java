    private void ObtenerDatosCreditos() {
        modelTabla.setRowCount(0);

        List<VistaCredito> creditos = new CreditoServices().obtenerCreditos();

        if (creditos != null) {
            for (VistaCredito c : creditos) {

                String[] renglon = {
                    String.valueOf(c.getId_credito()),
                    String.valueOf(c.getId_cliente()),
                    c.getNombre(),
                    c.getTelefono(),
                    c.getDireccion(),
                    String.valueOf(c.getId_factura()),
                    String.valueOf(c.getMonto_total()),
                    String.valueOf(c.getSaldo_pendiente()),
                    c.getEstado().toUpperCase(),
                    String.valueOf(c.getInteres()),
                    String.valueOf(c.getFecha_inicial()),
                    String.valueOf(c.getFecha_vencimiento())
                };

                modelTabla.addRow(renglon);
            }

            jTableCreditos.setModel(modelTabla);
        }
    }

